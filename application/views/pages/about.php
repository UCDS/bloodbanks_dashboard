 <h2><b> The Directorate of Medical Education </b></h2>
 <p>The <b> The Directorate of Medical Education </b>ensures the smooth functioning of all medical colleges and attached teaching hospitals, nursing schools and nursing colleges</p>
<ul>
The main functions and services of the Directorate are:

   <li> To provide Specialist Medical Care and medical facilities to people through hospitals.</li>
   <li> To impart quality Medical Education to undergraduates, P. G. and Super Speciality post graduates through Medical Colleges and Teaching hospitals.</li>
    <li>To provide training in Para-medical courses like nursing and medical and laboratory technology, etc. through Medical Colleges and Teaching Hospitals</li>
</ul>
The present department of Medical Education was originally called Department of Medical Services. It continued, as such, till Telangana State is going to be form on 02.06.2014 with 10 districts.

The present Directorate of Medical Education is to be create in the year 2014.
